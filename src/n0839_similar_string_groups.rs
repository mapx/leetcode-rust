impl Solution {
    pub fn num_similar_groups(a: Vec<String>) -> i32 {
        fn is_similar(s1: &str, s2: &str) -> bool {
            let mut diffs = 0;
            for (c1, c2) in s1.bytes().zip(s2.bytes()) {
                diffs += (c1 != c2) as usize;
                if diffs > 2 {
                    return false;
                }
            }
            diffs == 0 || diffs == 2
        }
        let len = a.len();
        let mut uf = UnionFind::new(len);
        let mut groups = len as i32;
        for (i, s1) in a.iter().enumerate() {
            for j in i..len {
                if is_similar(&s1, unsafe { a.get_unchecked(j) }) {
                    groups -= uf.union(i, j) as i32;
                }
            }
        }
        groups
    }
}

